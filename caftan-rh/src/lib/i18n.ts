export const TRANSLATIONS: Record<Locale, Record<TranslationKey, string>> = {
  fr: FR,
  nl: NL,
};

/**
 * Renvoie la traduction pour `key` dans `locale`. Fallback graceful sur FR si
 * absent en NL, puis sur la clé brute en dernier recours. Supporte des
 * variables `{name}` substituées dans la chaîne.
 */
export function t(
  key: TranslationKey,
  locale: Locale = "fr",
  vars?: Record<string, string | number>,
): string {
  let str: string =
    TRANSLATIONS[locale]?.[key] ?? TRANSLATIONS.fr[key] ?? String(key);
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.split(`{${k}}`).join(String(v));
    }
  }
  return str;
}

/**
 * Parse l'en-tête Cookie ou la valeur du cookie pour récupérer la locale.
 * Accepte un cookie header complet (`a=1; lang=nl; b=2`) ou juste `nl`.
 */
export function localeFromCookie(
  cookieValue?: string | null,
): Locale {
  if (!cookieValue) return "fr";
  const trimmed = cookieValue.trim();
  if (trimmed === "fr" || trimmed === "nl") return trimmed;
  const m = /(?:^|;\s*)lang=(fr|nl)/.exec(trimmed);
  return (m?.[1] as Locale) ?? "fr";
}

/** BCP-47 locale string utilisable dans `Date.prototype.toLocaleString`. */
export function dateLocaleStr(locale: Locale): string {
  return locale === "nl" ? "nl-BE" : "fr-BE";
}
